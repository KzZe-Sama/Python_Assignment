# For numbers and integers
item1=[20,50,60,10,90,20]
item2=[1500,2000,6000]
# ForString
item3=["John","Mary","Albert","Thomas","Jack"]
# Resulting Value Storing Var Initialization
totalA=0
totalB=0
totalC=""
# For Each Loop
for item in item1:
    totalA=totalA+item
for item in item2:
    totalB=totalB+item
for item in item3:
    totalC=totalC+item
print(totalA)
print(totalB)
print(totalC)


